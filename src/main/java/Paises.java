    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try {
            String nombre = campoNombre.getText();
            
            String sqlguardar = "INSERT INTO pais (nombre) VALUES (?)";
            PreparedStatement ps = coneccionsql.prepareStatement(sqlguardar);
            ps.setString(1, nombre);
            int n = ps.executeUpdate();
            if (n > 0) {
                Personas.comboPaises.addItem(nombre);
                campoNombre.setText("");
                JOptionPane.showMessageDialog(this, "Se guard√≥ correctamente");
            } else {
                JOptionPane.showMessageDialog(this, "Error al guardar");
            }
            ps.close();
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(rootPane, "Error: No se puede registrar el pais!");
        }
    }//GEN-LAST:event_jButton1MouseClicked


